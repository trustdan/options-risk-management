      <p class="koan-description">Increasing position size on underwater trades multiplies risk at precisely the time when the market is indicating your analysis may be wrong.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">The voice that says "just this once" is the voice of failure.</p>
