    </div>
    
    <div class="koan">
      <p class="koan-title">Trading less can lead to earning more.</p>
      <p class="koan-description">Selective engagement with only the highest probability setups maximizes capital efficiency and minimizes unnecessary transaction costs.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">A trader's greatest strength lies in knowing when to walk away.</p>
      <p class="koan-description">Recognizing unfavorable conditions or compromised mental states and stepping back temporarily preserves capital for future opportunities.</p>
