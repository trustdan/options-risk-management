      <p class="koan-description">Automatic trading cessation after reaching a specific drawdown threshold protects you from your worst decision-making moments.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">When on tilt, even good setups become bad trades.</p>
      <p class="koan-description">Compromised execution during emotional states ruins the expectancy of even statistically sound trading opportunities.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">Your self-awareness has a half-life; check it hourly.</p>
      <p class="koan-description">Regular scheduled assessment of your psychological state prevents the gradual, unnoticed emotional deterioration that precedes tilt.</p>
