      <p class="koan-description">Physical distance from trading tools provides the only reliable remedy for the vengeful impulses that follow painful losses.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">Tilt transforms trading from mathematics to gambling.</p>
      <p class="koan-description">Emotional reactivity replaces probabilistic thinking with hope-based decisions disconnected from edge and expectancy.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">The market will still be there tomorrow; your capital might not be.</p>
      <p class="koan-description">Temporary withdrawal from trading preserves resources that impulsive tilt-driven decisions would likely deplete permanently.</p>
    </div>
    
    <div class="koan">
      <p class="koan-title">Record your emotional state before each trade; patterns will emerge.</p>
      <p class="koan-description">Documenting your psychological condition reveals connections between certain emotional states and poor trading outcomes.</p>
